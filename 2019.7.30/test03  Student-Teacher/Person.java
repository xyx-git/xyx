package day07_test;
/*
 * 建立Person类  包含以下属性和方法
 *  name; age;height;tel;	
 *  getName()；getAge()；getHeight()；getTel()
 */
public class Person {
	String name;
	int age;
	int height;
	int tel;
	
	public String getName() {
		return name;
	}
	public int getAge() {
		return age;
	}
	public int getHeight() {
		return height;
	}
	public int getTel() {
		return tel;
	}
	
	
}
